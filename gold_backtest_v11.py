# 模拟历史资金费率（8小时结算，实际应从API获取）
# 黄金合约通常费率较低，我们基于价格走势模拟
def simulate_funding_rate(idx, bars):
    """基于价格动量模拟资金费率（无法获取真实历史费率时使用）
    真实场景应调用 okx market funding-rate XAU-USDT-SWAP
    """
    if idx < 24:
        return 0.0
    c = [b["close"] for b in bars[:idx+1]]
    # 短期涨幅大 → 多头拥挤 → 费率偏高（多头付费）
    ret_24h = (c[-1] - c[-24]) / c[-24] if c[-24] > 0 else 0
    # 基础费率 + 动量偏移
    base = 0.0001  # 黄金基础费率约0.01%
    fr = base + ret_24h * 0.01  # 涨幅越大费率越高
    return max(-0.002, min(0.002, fr))  # 限制范围

def ema(data, p):
    k = 2/(p+1); r=[data[0]]
    for v in data[1:]: r.append(v*k+r[-1]*(1-k))
    return r

def calc_atr(h, l, c, p=14):
    if len(h) < 2: return h[0]-l[0]
    trs = [h[0]-l[0]] + [max(h[i]-l[i], abs(h[i]-c[i-1]), abs(l[i]-c[i-1])) for i in range(1, len(h))]
    if len(trs) < p: return sum(trs)/len(trs)
    e = sum(trs[:p])/p; k = 2/(p+1)
    for tr in trs[p:]: e = tr*k + e*(1-k)
    return e

def rsi(closes, p=14):
    if len(closes) <= p: return 50.0
    g = [max(closes[i]-closes[i-1], 0) for i in range(1, len(closes))]
    l_ = [max(-(closes[i]-closes[i-1]), 0) for i in range(1, len(closes))]
    ag = sum(g[-p:])/p; al = sum(l_[-p:])/p
    return 100-(100/(1+ag/al)) if al else 100

def boll(closes, p=20, m=2):
    if len(closes) < p: return None, None, None
    sma = sum(closes[-p:])/p
    std = math.sqrt(sum((c-sma)**2 for c in closes[-p:])/p)
    return sma+m*std, sma, sma-m*std

def score_signal(idx, bars):
    """v1.1: 加入资金费率过滤，降低信号阈值"""
    if idx < 50: return 0, [], 0, 0  # score, sigs, atr, funding

    c = [b["close"] for b in bars[:idx+1]]
    h = [b["high"] for b in bars[:idx+1]]
    l_ = [b["low"] for b in bars[:idx+1]]
    v = [b["vol"] for b in bars[:idx+1]]
    last = c[-1]; h24 = max(c[-24:]); lo24 = min(c[-24:])
    rng = h24 - lo24; price_pos = (last-lo24)/rng if rng > 0 else 0.5
    e20 = ema(c, 20)[-1]; e50 = ema(c, 50)[-1]; ed = (e20-e50)/e50*100
    rsi_v = rsi(c); bb_u, bb_m, bb_l = boll(c)
    avg_v = sum(v[-24:])/24; vr = v[-1]/avg_v if avg_v > 0 else 1.0
    cur_atr = calc_atr(h, l_, c, PARAMS["atr_period"])
    fr = simulate_funding_rate(idx, bars)

    score = 0; sigs = []

    # === 维度1: 价格位置 (25%) ===
    if price_pos < 0.15: score += 1.5; sigs.append(f"price极低{price_pos:.2f}(+1.5)")
    elif price_pos < 0.30: score += 1.2; sigs.append(f"price低位{price_pos:.2f}(+1.2)")
    elif price_pos < 0.50: score += 0.8; sigs.append(f"price中低{price_pos:.2f}(+0.8)")
    elif price_pos < 0.70: score += 0.4; sigs.append(f"price中高{price_pos:.2f}(+0.4)")
    elif price_pos < 0.85: score += 0; sigs.append(f"price偏高{price_pos:.2f}")
    else: score -= 0.5; sigs.append(f"price极高{price_pos:.2f}(-0.5)")

    # === 维度2: EMA趋势 (20%) ===
    if ed > 1.0: score += 1.25; sigs.append(f"EMA多头+{ed:.2f}%(+1.25)")
    elif ed > 0.3: score += 0.8; sigs.append(f"EMA偏多+{ed:.2f}%(+0.8)")
    elif ed > -0.3: score += 0.3; sigs.append(f"EMA震荡{ed:.2f}%")
    elif ed > -1.0: score += 0.5; sigs.append(f"EMA偏空{ed:.2f}%(+0.5)")
    else: score += 0.8; sigs.append(f"EMA空头{ed:.2f}%(+0.8)")

    # === 维度3: ATR波动率 (15%) ===
    avg_atr = cur_atr  # 当前ATR
    if avg_atr > 0:
        atr_pct = avg_atr / last * 100
        if atr_pct > 1.0:  # 高波动
            score += 0.3; sigs.append(f"ATR高波动{atr_pct:.2f}%(+0.3)")
        elif atr_pct > 0.5:  # 中波动
            score += 0.6; sigs.append(f"ATR中波动{atr_pct:.2f}%(+0.6)")
        else:  # 低波动
            score += 0.4; sigs.append(f"ATR低波动{atr_pct:.2f}%(+0.4)")

    # === 维度4: 资金费率过滤 (15%) ★新增 ===
    if fr < PARAMS["funding_long_favorable"]:
        score += 0.75; sigs.append(f"费率有利{fr*100:.3f}%(+0.75)")
    elif fr < 0.0001:
        score += 0.3; sigs.append(f"费率中性{fr*100:.3f}%(+0.3)")
    elif fr < PARAMS["funding_long_caution"]:
        score -= 0.3; sigs.append(f"费率偏多付{fr*100:.3f}%(-0.3)")
    elif fr >= PARAMS["funding_long_forbidden"]:
        score -= 1.0; sigs.append(f"费率极端{fr*100:.3f}%(-1.0)")
    else:
        score -= 0.5; sigs.append(f"费率偏高{fr*100:.3f}%(-0.5)")

    # === 维度5: RSI/布林带 (15%) ===
    if rsi_v < 30: score += 0.75; sigs.append(f"RSI超卖{rsi_v:.1f}(+0.75)")
    elif rsi_v < 40: score += 0.5; sigs.append(f"RSI偏低{rsi_v:.1f}(+0.5)")
    elif rsi_v > 70: score -= 0.5; sigs.append(f"RSI超买{rsi_v:.1f}(-0.5)")
    elif rsi_v > 60: score -= 0.2; sigs.append(f"RSI偏高{rsi_v:.1f}(-0.2)")
    else: score += 0.2; sigs.append(f"RSI中性{rsi_v:.1f}")

    if bb_l and last <= bb_l: score += 0.4; sigs.append("BB下轨(+0.4)")
    elif bb_u and last >= bb_u: score -= 0.3; sigs.append("BB上轨(-0.3)")
    elif bb_l and last < bb_m: score += 0.2; sigs.append("BB中下(+0.2)")

    # === 维度6: 成交量 (10%) ===
    if vr > 2.0: score += 0.5; sigs.append(f"放量{vr:.1f}x(+0.5)")
    elif vr > 1.5: score += 0.3; sigs.append(f"温和放量{vr:.1f}x(+0.3)")
    elif vr < 0.3: score -= 0.2; sigs.append(f"缩量{vr:.1f}x(-0.2)")
    else: score += 0.1

    return round(score, 2), sigs, cur_atr, fr

def run_backtest(bars, capital=10000):
    pos = 0; ep = 0; side = None; et = None; eatr = 0
    tc = 0; w = 0; l_ = 0; trades = []; eq = [capital]
    dt = {}; peak = capital; maxdd = 0
    leverage = PARAMS["leverage"]

    for i, bar in enumerate(bars):
        dt_str = bar["time"]; d = dt_str.split()[0]
        hi = bar["high"]; lo = bar["low"]; cl = bar["close"]

        # 权益
        if pos > 0:
            pnl = (cl-ep)*pos*0.1 if side == "long" else (ep-cl)*pos*0.1
            equity = capital + pnl
        else:
            equity = capital
        eq.append(equity)
        if equity > peak: peak = equity
        dd = (peak-equity)/peak*100
        if dd > maxdd: maxdd = dd

        # 平仓检查 (ATR动态止损)
        if pos > 0 and eatr > 0:
            sl_dist = eatr * PARAMS["atr_sl_mult"]
            tp_dist = eatr * PARAMS["atr_tp_mult"]
            if side == "long":
                pnl = (cl-ep)*pos*0.1
                if cl <= ep - sl_dist:
                    real_pnl = (cl-ep)*pos*0.1
                    capital += real_pnl; l_ += 1; tc += 1
                    trades.append({"date":dt_str,"side":"long","entry":ep,"exit":cl,"size":pos,"pnl":round(real_pnl,2),"reason":"SL","atr":round(eatr,2),"sl_px":round(ep-sl_dist,2),"tp_px":round(ep+tp_dist,2)})
                    pos = 0; side = None
                elif cl >= ep + tp_dist:
                    real_pnl = (cl-ep)*pos*0.1
                    capital += real_pnl; w += 1; tc += 1
                    trades.append({"date":dt_str,"side":"long","entry":ep,"exit":cl,"size":pos,"pnl":round(real_pnl,2),"reason":"TP","atr":round(eatr,2),"sl_px":round(ep-sl_dist,2),"tp_px":round(ep+tp_dist,2)})
                    pos = 0; side = None
                elif et is not None and i - et > 48:
                    capital += pnl; tc += 1
                    trades.append({"date":dt_str,"side":"long","entry":ep,"exit":cl,"size":pos,"pnl":round(pnl,2),"reason":"TIME","atr":round(eatr,2),"sl_px":round(ep-sl_dist,2),"tp_px":round(ep+tp_dist,2)})
                    pos = 0; side = None

        # 开仓 (信号阈值1.8)
        if pos == 0 and dt.get(d, 0) < PARAMS["max_daily_trades"]:
            sc, sigs, cur_atr, fr = score_signal(i, bars)
            if sc >= PARAMS["signal_threshold"]:
                # 资金费率极端禁止做多
                if fr >= PARAMS["funding_long_forbidden"]:
                    continue
                max_p = int((capital * PARAMS["max_position_pct"]) / (cl * 0.1 / leverage))
                sz = min(10, max(1, max_p))
                pos = sz; ep = cl; side = "long"; et = i; eatr = cur_atr
                dt[d] = dt.get(d, 0) + 1

    # 期末平仓
    if pos > 0:
        lc = bars[-1]["close"]
        pnl = (lc-ep)*pos*0.1
        capital += pnl
        trades.append({"date":bars[-1]["time"],"side":"long","entry":ep,"exit":lc,"size":pos,"pnl":round(pnl,2),"reason":"END","atr":round(eatr,2)})

    # 统计
    total = tc; wr = w/total*100 if total > 0 else 0
    ret = (capital-10000)/10000*100
    ann = (capital/10000)**(365/30)-1
    mxw = max([t["pnl"] for t in trades], default=0)
    mxl = min([t["pnl"] for t in trades], default=0)
    wt = [t for t in trades if t["pnl"] > 0]; lt = [t for t in trades if t["pnl"] < 0]
    avgw = sum(t["pnl"] for t in wt)/len(wt) if wt else 0
    avgl = abs(sum(t["pnl"] for t in lt)/len(lt)) if lt else 0
    pf = (avgw*len(wt))/avgl if avgl > 0 else float("inf")
    pnls = [t["pnl"] for t in trades]
    avg_p = sum(pnls)/len(pnls) if pnls else 0
    std_p = math.sqrt(sum((p-avg_p)**2 for p in pnls)/max(len(pnls)-1, 1)) if len(pnls) > 1 else 0
    sharpe = avg_p/std_p if std_p > 0 else 0

    return {
        "version": "v1.1", "params": PARAMS, "initial": 10000, "final": round(capital, 2),
        "return_pct": round(ret, 2), "ann_return_pct": round(ann*100, 2),
        "total_trades": total, "wins": len(wt), "losses": len(lt), "win_rate": round(wr, 1),
        "max_dd": round(maxdd, 2), "max_win": round(mxw, 2), "max_loss": round(mxl, 2),
        "avg_win": round(avgw, 2), "avg_loss": round(avgl, 2), "profit_factor": round(pf, 2),
        "sharpe": round(sharpe, 2), "trades": trades,
    }

